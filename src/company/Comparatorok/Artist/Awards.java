package company.Comparatorok.Artist;

public abstract class Awards {
    private String awardName;
    private int awardedIn;

    public String getAwardName() {
        return awardName;
    }

    public void setAwardName(String awardName) {
        this.awardName = awardName;
    }

    public int getAwardedIn() {
        return awardedIn;
    }

    public void setAwardedIn(int awardedIn) {
        this.awardedIn = awardedIn;
    }
}
